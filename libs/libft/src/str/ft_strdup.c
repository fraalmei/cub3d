#include <libft.h>

char	*ft_strdup(const char *s1)
{
	size_t	size;
	char	*ptr;

	size = ft_strlen(s1) + 1;
	ptr = (char *)ft_calloc(sizeof(char), size);
	if (ptr == NULL)
		return (NULL);
	ft_strlcpy(ptr, s1, size);
	return ((char *)ptr);
}
